package day_15;



public class RangeOfAlphabet {
    public static void main(String[] args) {

        /**
         * Create 2 char variables called firstLetter and lastLetter.
         * print all the letters between firstLetter and lastLetter.
          */

        char firstLetter = 'k';
        char lastLetter = 'z';

        for (int i = firstLetter; i<=lastLetter; i++){
            System.out.println((char)i);
        }


    }
}
