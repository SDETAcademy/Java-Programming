package day_13;

public class WhileLoop {
    public static void main(String[] args) {

        int apple = 0;

        while(apple<=5){
            System.out.println("The number of apple is " + apple);
            apple++;
        }

        /**
         * Create a variable named oranges;
         * create while loop (if # oranges <25) - print them
         */

        int oranges = 0;
        while(oranges<25){
            System.out.println("Number of oranges: " + oranges);
            oranges++;
        }


    }
}
